package vn.edu.saigontech.source.Model;


public class StudentInformation {
	private Integer ID;
	private String studentID;
	private String hccsPID;
	private String hccsID;
	private String hccsLIB;
	private String TOEFL;
	private String lastName, firstName;
	private String DOB;
	private String Gender;
	private String Course;
	private String parentName;
	private String phoneNumber;
	private String Email;
	private String permanentAD;
	private String mailAD;
	private String Image;
	
	public StudentInformation() {
		super();
	}

	public Integer getID() {
		return ID;
	}

	public void setID(Integer iD) {
		ID = iD;
	}

	public String getStudentID() {
		return studentID;
	}

	public void setStudentID(String studentID) {
		this.studentID = studentID;
	}

	public String getHccsPID() {
		return hccsPID;
	}

	public void setHccsPID(String hccsPID) {
		this.hccsPID = hccsPID;
	}

	public String getHccsID() {
		return hccsID;
	}

	public void setHccsID(String hccsID) {
		this.hccsID = hccsID;
	}

	public String getHccsLIB() {
		return hccsLIB;
	}

	public void setHccsLIB(String hccsLIB) {
		this.hccsLIB = hccsLIB;
	}

	public String getTOEFL() {
		return TOEFL;
	}

	public void setTOEFL(String tOEFL) {
		TOEFL = tOEFL;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getDOB() {
		return DOB;
	}

	public void setDOB(String dOB) {
		DOB = dOB;
	}

	public String getGender() {
		return Gender;
	}

	public void setGender(String gender) {
		Gender = gender;
	}

	public String getCourse() {
		return Course;
	}

	public void setCourse(String course) {
		Course = course;
	}

	public String getParentName() {
		return parentName;
	}

	public void setParentName(String parentName) {
		this.parentName = parentName;
	}

	public String getPhoneNumber() {
		return phoneNumber;
	}

	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}

	public String getEmail() {
		return Email;
	}

	public void setEmail(String email) {
		Email = email;
	}

	public String getPermanentAD() {
		return permanentAD;
	}

	public void setPermanentAD(String permanentAD) {
		this.permanentAD = permanentAD;
	}

	public String getMailAD() {
		return mailAD;
	}

	public void setMailAD(String mailAD) {
		this.mailAD = mailAD;
	}

	public String getImage() {
		return Image;
	}

	public void setImage(String image) {
		Image = image;
	}
	
	
}

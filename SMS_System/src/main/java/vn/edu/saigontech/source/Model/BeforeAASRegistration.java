package vn.edu.saigontech.source.Model;
/** This Object is used to contain Before AAS Registration information
 * @author Tạ Minh Triết
 * Written on 22/3/2018
 */ 
public class BeforeAASRegistration {
	private String beforeAASRegistraion;
	
	public BeforeAASRegistration() {}

	public BeforeAASRegistration(String beforeAASRegistraion) {
		this.beforeAASRegistraion = beforeAASRegistraion;
	}

	public String getBeforeAASRegistraion() {
		return beforeAASRegistraion;
	}

	public void setBeforeAASRegistraion(String beforeAASRegistraion) {
		this.beforeAASRegistraion = beforeAASRegistraion;
	}
	
	
}

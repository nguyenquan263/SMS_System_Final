package vn.edu.saigontech.source.Model;

/** This class represents the student's average GPA
 * @author Nguyen Bao Hoang Long
 * Written on 23/3/2018
 */
public class HCCTranscriptAverageGPA {
	private double averageGPA;

	public HCCTranscriptAverageGPA(double averageGPA) {
		super();
		this.averageGPA = averageGPA;
	}

	public double getAverageGPA() {
		return averageGPA;
	}

	public void setAverageGPA(double averageGPA) {
		this.averageGPA = averageGPA;
	}
	
	
}

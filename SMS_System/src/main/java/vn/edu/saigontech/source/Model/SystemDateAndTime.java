package vn.edu.saigontech.source.Model;

/** This class represents system current date and time
 * @author Nguyen Bao Hoang Long
 * Written on 24/3/2018
 */
public class SystemDateAndTime {
	private String dateAndTime;

	public SystemDateAndTime(String dateAndTime) {
		super();
		this.dateAndTime = dateAndTime;
	}

	public String getDateAndTime() {
		return dateAndTime;
	}

	public void setDateAndTime(String dateAndTime) {
		this.dateAndTime = dateAndTime;
	}
	
	
}
